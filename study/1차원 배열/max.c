#include <stdio.h>

int main() {

	int num_ary[9];
	int max_value=1, max_index=1;

	for (int i = 0; i < 9; i++) {
		scanf("%d", &num_ary[i]);
	}


	for (int i = 0; i < 9; i++) {
		if (max_value < num_ary[i]) {
			max_value = num_ary[i];
			max_index = i+1;
		}
	}
	
	printf("%d\n%d", max_value, max_index);

	return 0;
}
